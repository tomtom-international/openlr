package openlr.decoder.rating.critics;

public class LinearRating implements RatingFunction {

    private final double threshold;
    private final double maxScore;

    public LinearRating(double threshold, double maxScore) {
        this.threshold = threshold;
        this.maxScore = maxScore;
    }

    @Override
    public int rate(double value) {
        double absoluteValue = Math.abs(value);
        if (absoluteValue > threshold) {
            return 0;
        }
        return (int) ((1.0 - absoluteValue / threshold) * maxScore);
    }
}
